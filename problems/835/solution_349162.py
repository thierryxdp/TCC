def melhor_volta(matriz):
    lista = []
    for linha in matriz:
        list.append(lista, min(linha))
    tempo_melhor_volta = min(lista)
    melhor_volta = list.index(matriz[list.index(lista, tempo_melhor_volta)],
                              tempo_melhor_volta)
    resposta = (list.index(lista, tempo_melhor_volta) + 1,
                tempo_melhor_volta, melhor_volta + 1)