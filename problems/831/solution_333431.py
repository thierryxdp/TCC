def lingua_p(palavra):
    var = palavra
    n = len(palavra)
    for x in range(1,n+1):
        if x == 'a':
            var[x] = 'apa'
        if x == 'e':
            var[x] = 'epe'
        if x == 'i':
            var[x] = 'ipi'
        if x == 'o':
            var[o] = 'opo'
        if x == 'u':
            var[u] = 'upu'
    return var