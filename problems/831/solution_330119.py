def lingua_p(palavra):
    y = palavra.split('a')
    y = palavra.replace('a',"apa")
    y = palavra.split('e')
    y = palavra.replace('e',"epe")
    y = palavra.split('i')
    y = palavra.replace('i',"ipi")
    y = palavra.replace('o')
    y = palavra.replace('o',"opo")
    y = palavra.replace('u')
    y = palavra.replace('u',"upu")
    return y