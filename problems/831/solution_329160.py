def lingua_p (palavra):
    '''
    	essa função recebe uma palavra que e a traduz para a linguagem P
        str-->str
    '''
    x = str.replace(str.replace(str.replace(str.replace(str.replace(str.replace(str.replace(str.replace(str.replace(str.replace(palavra, 'a', 'apa'), 'e', 'epe'), 'i', 'ipi'), 'o', 'opo'), 'u', 'upu'), 'á', 'ápá'),'é', 'épé'), 'í', 'ípí'), 'ó', 'ópó'), 'ú', 'úpú')
    y = str.lower(x)
    return y