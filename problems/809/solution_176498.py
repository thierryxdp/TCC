def intercala(lista1, lista2):
    """Fazer uma terceira lista a partir da primeira e segunda intercalando os seus valores;
    list, list -> list"""
    
    lista3 = []
    
    for i in range(0, len[lista1[i]]):
        lista3.append(lista1[i])
        lista3.append(lista2[i])
        
    return lista3