def intercala(lista1, lista2):
    """Dados os valores da lista1 e lista2 como entrada, retorna concatação entre lista1 e lista2 de forma intercalda"""
    return lista1[0]+lista2[0]+lista1[1]+lista2[1]+lista1[2]+lista1[2]