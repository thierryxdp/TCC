# intercala duas listas dadas, retornando uma terceira lista com os elementos das duas primeiras intercalados
# lista1, lista2
def intercala(lista1, lista2):
    """intercala duas listas dadas, retornando uma terceira lista com os elementos das duas primeiras intercalados
       list,list-->list."""
    lista3=[lista1[0],lista2[0],lista1[1],lista2[1],lista1[2],lista2[2]]
    return lista3