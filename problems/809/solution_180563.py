def intercala(lista1, lista2):
    """ Dadas duas listas de tamanho 3, L1 e L2, a funcao intercala os valores
    das duas listas em uma nova lista L3"""
    
    
    L1 = lista1
    L2 = lista2
    
    p1 = L1[0]
    p2 = L2[0]
    p3 = L1[1]
    p4 = L2[1]
    p5 = L1[2]
    p6 = L2[2]
    
    L3 = [p1, p2, p3, p4, p5, p6]
    
    
    return L3