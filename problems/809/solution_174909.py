def intercala(lista1, lista2):
    """função que retorne duas listas L1 e L2 de tamanho 3 intercalando uma com a outra; str,str-> int, int, int, int, int, int"""
    L1= lista1('[0]')
    L2= lista2('[0]')
    L3= lista1('[1]')
    L4= lista2('[1]')
    L5= lista1('[2]')
    L6= lista2('[2]')
    L= L1+L2+L3+L4+L5+L6
    return L