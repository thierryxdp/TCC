def intercala(lista1, lista2):
    """Função que recebe duas listas (lista1 e lista2) de tamanho 3 e retorna uma nova lista3 formada a partir dos elementos 
    das duas listas de entrada intercalados; list,list->list"""
    lista3=[lista1[0]+lista2[0]+lista1[1]+lista2[1]+lista1[2]+lista2[2]]
    return lista3