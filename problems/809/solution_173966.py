def intercala(lista1, lista2):
    """Dadas duas listas de tamanho 3, gera uma lista L3 intercalando
    os elementos das duas"""
    L3 = [lista1[0], lista2[0], lista1[1], lista2[1], lista1[2], lista2[2]]
    return L3