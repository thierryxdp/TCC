def intercala(lista1, lista2):
    """Função cria uma nova lista intercalando os elementos da lista1 e lista2.;
    Necessário que a lista tenha 3 elementos.
    list, list -> list"""
    lista3 = lista1[0] + lista2 [0] + lista1 [1] + lista2 [1] + lista1 [2] + lista2 [2]
    return list (lista3)