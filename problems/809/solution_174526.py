def intercala(lista1, lista2):
    """Recebe duas listas com 3 itens cada e retorna uma 
    terceira lista com os elementos de cada lista
    intercalados.
    list -> list"""
    a = list((lista1[0],))
    b = list((lista2[0],))
    c = list((lista1[1],))
    d = list((lista2[1],))
    e = list((lista1[2],))
    f = list((lista2[2],))
    return a+b+c+d+e+f