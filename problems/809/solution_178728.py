def intercala(lista1, lista2):
    """Função que, dadas duas listas L1 e L2 de tamanho 3,
    gera uma lista L3 que é formada intercalando os elementos
    de L1 e L2."""
    e11=lista1[0]
    e12=lista1[1]
    e13=lista1[2]
    e21=lista2[0]
    e22=lista2[1]
    e23=lista2[2]
    return [e11,e21,e12,e22,e13,e23]