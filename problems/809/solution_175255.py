def intercala(lista1, lista2):
    '''função que intercala os elementos das duas listas dadas.
    lista -> lista'''
    
    elem1 = lista1[0]
    elem2 = lista2[0]
    elem3 = lista1[1]
    elem4 = lista2[1]
    elem5 = lista1[2]
    elem6 = lista2[2]
    
    return elem1 + elem2 + elem3 + elem4+ elem5 + elem6