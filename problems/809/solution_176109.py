def intercala(lista1, lista2):
    """Intercala os elementos do parâmetro de entrada
    lista1 e lista2 e retorna a lista intercalado
    list,list->list"""
    x1,x2,x3=lista1
    y1,y2,y3=lista2
    lista_final=[x1,y1,x2,y2,x3,y3]
    return lista_final