def intercala(lista1, lista2):
    """intercala recebe duas listas e devolve uma nova lista que é formada intercalando os elementos da lista1 e lista 2.
    list,list-->list"""
    lista=[lista1[0],lista2[0],lista1[1],lista2[1],lista1[2],lista2[2]]
    return lista