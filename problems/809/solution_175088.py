def intercala(lista1, lista2):
    """Função que dadas duas listas gera uma teceira, intercalando os elementos das duas primeiras
    list, list-> list"""
    x = lista1
    y = lista2
    a = x[0]
    b = x[1]
    c = x[2]
    d = y[0]
    e = y[1]
    f = y[2]
    return (a, d, b, e, c, f)