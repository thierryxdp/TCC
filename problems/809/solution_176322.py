def intercala(lista1, lista2):
    """dadas duas listas L1 e L2, de tamanho 3, gera uma
    lista L3 que intercala os elementos das mesmas
    list,list->list"""
    a=lista1[0]
    b=lista2[0]
    c=lista1[1]
    d=lista2[1]
    e=lista1[2]
    f=lista2[2]
    return [a] + [b] + [c] + [d] + [e] + [f]