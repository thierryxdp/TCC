def intercala(lista1, lista2):
        lista3=['lista']
        lista3=lista3.append(lista1[0])
        lista3=lista3.append(lista2[0])
        lista3=lista3.append(lista1[1])
        lista3=lista3.append(lista2[1])
        lista3=lista3.append(lista1[2])
        lista3=lista3.append(lista2[2])
        lista3.replace('lista','')
        return lista3