def intercala(lista1, lista2):
    """A função recebe como entrada duas listas (lista1 e lista2) e ao ser chamada retorna uma
    nova lista com os elementos das duas originais intercalados."""
    n1 = lista1[0]
    n2 = lista2[0]
    n3 = lista1[1]
    n4 = lista2[1]
    n5 = lista1[2]
    n6 = lista2[2]
    l3 = [n1, n2, n3, n4, n5, n6]
    return l3