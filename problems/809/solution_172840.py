def intercala(lista1, lista2):
    """função que dada duas listas L1 e L2 de tamanho 3, gera uma lista L3 que é formada intercalando os elementos L1 e L2;
    lista -> lista"""
    var1 = lista1[0]
    var2 = lista2[0]
    var3 = lista1[1]
    var4 = lista2[1]
    var5 = lista1[2]
    var6 = lista2[2]
    return [var1,var2,var3,var4,var5,var6]