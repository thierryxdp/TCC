def uppCons(frase):
    """A função recebe uma frase e retorna a mesma frase com todas as consoantes em maiúsculo;
    str->str"""
    str.upper(frase)
    str.replace(frase,"A","a")
    str.replace(frase,"E","e")
    str.replace(frase,"I","i")
    str.replace(frase,"O","o")
    str.replace(frase,"U","u")