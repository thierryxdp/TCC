def uppCons(frase):
    frase = frase.upper()
    for x in frase:
        if x == "A":
            x.lower()
        elif y == "E":
            y.lower()
        elif z == "I":
            z.lower()    
        elif h == "O":
            h.lower()
        elif g == "U":
            g.lower()
    return frase