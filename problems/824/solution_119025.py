def uppCons(frase):
    frase = frase.upper()
    for x in frase:
        if x == "A":
            x.lower()
        elif x == "E":
            x.lower()
        elif x == "I":
            x.lower()    
        elif x == "O":
            x.lower()
        elif x == "U":
            x.lower()
    return frase