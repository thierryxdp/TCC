def uppCons (frase):
    '''
    	Essa função recebe uma frase e retorna a mesma com todas as suas consoantes maiusculas e 
        suas demais caracteres são mantidos como estavam
        str->str
    '''
    if 'b' or 'c' or 'd' or 'f' or 'g' or 'h' or 'j' or 'k' or 'l' or 'm' or 'n' or 'p' or 'q' or 'r' or 's' or 't' or 'v' or 'w' or 'x'or 'y' or 'z'in frase:
        frase = frase.upper
    return frase