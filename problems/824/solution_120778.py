def uppCons(frase):
    """função que dada uma frase retorna a mesma frase com as consoantes em maiúsculas.
    str -> str"""
    frase = []
    consoantes = ['b'], ['c'], ['d'], ['f'], ['g'], ['h'], ['j'], ['k'], ['l'], ['m'], ['n'], ['p'], ['q'], ['r'], ['s'], ['t'], ['v'], ['w'], ['x'], ['y'], ['z']
    while consoantes in frase:
        return (consoantes.upper() in frase)