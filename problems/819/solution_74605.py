def filtraMultiplos(lista,n):
    '''funcao que recebe como entrada uma lista de numeros e retorna outra lista contendo todos os elementos da lista divisiveis por n
    lista, int->lista'''
    lista2=[]
    i=0
    while i<len(lista):
        lista[i]=a
