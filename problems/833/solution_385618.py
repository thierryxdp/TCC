def conta_numero(numero,matriz):
    '''
    	essa função recebe uma matriz e conta a quantidades de vezes que um determinado 
        numero aparece na mesma.
        num, list -> num
    '''
    qtd = []
    for x in matriz:
        qtd_2 = 0
        if x == numero:
            qtd_2 = qtd_2 + 1
        list.append(qtd, qtd_2)
    return qtd