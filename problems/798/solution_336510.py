def freq_palavras(frases):
    """A entrada é um string de frases que serão colocadas
    dentro do parâmetro e o retorno é um dicionário com 
    cada palavra e a quantidade de vezes que ela aparece"""
    #str -> dici, list
    dicionario = {}
    palavras = frases.split(" ")
    for palavra in palavras:
        palavras.count("palavra")
        dicionario[palavra] = palavras.count(palavra)