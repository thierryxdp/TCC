def freq_palavras(frases):
    '''funçao que recebe uma string e retorna um dicionário
    onde cada palavra dessa string é uma chave e tem como valor
    o número de vezes que a palavra aparece; str->dici'''
    n=0
    palavra=''
    frases={palavra:n}
    resultado=[]
    for palavra in frases:
        if str.count[frases]:
            resultado+=palavra
    return resultado