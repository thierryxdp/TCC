def carros(x):
    """Calcula e retorna a quantidade de carros necessário para uma vaigem. Entrada -> int; Saída -> int"""
    return x//5
def carros_nc(x,y):
    """Calcula e retorna a quantidade de carros necessário para uma vaigem, onde y é o nuúmero de capacidado do carro. Entrada -> int; Sáida -> int"""
    return x//y