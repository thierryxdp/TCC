def retira_pontuacao(texto):
    '''Função que, dado um texto, retorna esse texto com as pontuações subsituidas por espaços.
    str --> str'''
    textoespacado = str.replace(str.replace(str.replace(str.replace(str.replace(str.replace(str.replace(str.replace(texto,'...',' '),',',' '),'-',' '),'!',' '),'?',' '),'.',' '),';',' '),':',' ')
    return textoespacado