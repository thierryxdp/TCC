def retira_pontuacao(frase):
    '''Função que dada uma frase como parâmetro de entrada, retorna essa mesma frase 
    com espaço vazio no lugar da pontuação
    str->str'''
    return str.replace(str.replace(str.replace(str.replace(str.replace(str.replace(str.replace(frase,'!',' '),'?',' '),'-',' '),':',' '),';',' '),'.',' '),',',' ')