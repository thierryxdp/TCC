def retira_pontuacao(frase):
    '''Função que dada uma frase de entrada retire suas 
    pontuações e retorne a mesma frase com espaço no lugar
    das mesmas. string --> string.'''
    frase = frase.replace(";"," ")
    frase = frase.replace("-"," ")
    frase
    frase = frase.replace("!"," ")
    frase = frase.replace("?"," ")
    frase
    frase = frase.replace(","," ")
    frase = frase.replace(":"," ")
    frase
    frase = frase.replace("."," ")
    return frase