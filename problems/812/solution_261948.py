def retira_pontuacao(texto):
    lista_vazia = ()
    lista = str.replace(texto, "-", )
    lista = str.replace(lista, ",", lista_vazia)
    lista = str.replace(lista, ":", lista_vazia)
    lista = str.replace(lista, ";", lista_vazia)
    lista = str.replace(lista, ".", lista_vazia)
    
    return (lista)