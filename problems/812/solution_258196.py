def retira_pontuacao (frase):
    '''funçao que substitui todas as pontuaçoes de um dado dexto por espeaço invisivel
    	str->str'''
    frase = frase.replace(";"," ")
    frase = frase.replace("-"," ")
    frase
    frase = frase.replace("!"," ")
    frase = frase.replace("?"," ")
    frase
    frase = frase.replace(","," ")
    frase = frase.replace(":"," ")
    frase
    frase = frase.replace("."," ")
    return frase