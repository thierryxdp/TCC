def retira_pontuacao(frase):
    """ retorna a frase onde todos os caracteres de pontuação tenham sido substituídos por espaço str-->str"""
    d=str.replace(frase,","," ")
    c=str.replace(frase,"."," ")
    b=str.replace(frase,":"," ")
    a=str.replace(frase,"-"," ")
    z=str.replace(frase,";"," ")
    y=str.replace(frase,"!"," ")
    x=str.replace(frase,"?"," ")
    return 'x'+'y'+'z'+'a'+'b'+'c'+'d'