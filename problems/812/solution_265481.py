def retira_pontuacao(frase):
    '''função que dada uma frase, ela substitui todos os 
    caracteres de pontuação por espaço;
       str -> str'''
    f=frase
    return str.replace(str.replace(str.replace(str.replace(str.replace(str.replace(str.replace(f,'!',' '),'?',' '),'.',' '),'-',' '),',',' '),';',' '),':',' ')