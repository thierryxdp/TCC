def retira_pontuacao(frase):
    ''' Recebe uma frase e retorna-a substituindo todos os caracteres de pontuação por espaços vazios;
    str => str'''
    if int(str.find(frase,'...'))==-1:
        return str.replace(frase,'-'and','and':'and';'and'!'and'?'and'.',' ')
    else:
        return str.replace(frase,'-'and','and':'and';'and'!'and'?'and'...',' ')