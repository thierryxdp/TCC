def concatenacao(a, b):
    """Recebe duas strings 'a' e 'b' e retorna uma nova string no formato
    'abba'. No caso de números, não é necessário o usuário colocar as aspas
    simples"""
    return str(a)+str(b)+str(b)+str(a)