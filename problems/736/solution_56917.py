# str, str -> str
def concatenacao(a, b):
    """ Função que realiza a concatenação de duas strings à
   escolha do usuário, a e b, no formato abba;
    str, str -> str"""
    x = str(a) + str(b)
    y = str(b) + str(a)
    return x + y