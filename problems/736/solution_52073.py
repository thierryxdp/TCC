def concatenacao(a, b):
    """A função faz a concatenação de duas strings à escolha do usuário no formato abba. Os parametros são
    a e b (duas strings) e o retorno será a concatenação no formato abba.
	
    str, str -> str
    
    exemplos:
    concatenacao("a","b")=="abba"
    
    
    """
    return a+b+b+a