# str, str -> str
def concatenacao(a, b):
    """Retorna a concatenação de duas strings a e b no formato abba.
    Testes: concatenacao(1, 2) == 1221
    concatenacao(2, 1) == 2112
    Assinatura: str, str --> str
    """
    return str(a) + str(b)