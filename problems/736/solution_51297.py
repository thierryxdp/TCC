def concatenacao (a, b):
    """ Esta função retorna a concatenação delas no formato abba
        Parameters:
            a = primeira string a ser inserida (deve ser digitada entre aspas)
            b = segunda string a ser inserida (deve ser digitada entre aspas)
        Testes:
            concatenacao ("oi","tudo bem?") = "oitudo bem?tudo bem?oi"
            concatenacao ("1","2") = "1221"

        Returns:
            Taxa de desconto de imposto de INSS de acordo com a tabela do INSS
            str, str -> str.
    """

    return a+b+b+a