def num_bombons (p,d):
    ''' Planejamento
    Calcular a quantidade maxima de bombons que consegue comprar, sabendo o preço do bombom e o dinheiro que possui
    Parâmetros:
    p,d: float ; p: preço do bombom; d: dinheiro possuido
    Return: d/p ; int
    Um numero que nos dê a quantidade de bombons que é possivel comprar'''
    return int (p/d)