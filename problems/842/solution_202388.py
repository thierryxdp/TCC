def pontos_por_time(ls):
    g1 = []
    g2 = []
    if ls[0][2][0] > ls[0][2][1]:
        g1 = [0, 3] 
    if ls[0][2][0] < ls[0][2][1]:
        g1 = [3, 0]
    else:
        g1 = [1, 1]
    if ls[1][2][0] > ls[1][2][1]:
        g2 = [3, 0] 
    if ls[1][2][0] < ls[1][2][1]:
        g2 = [0, 3]
    else:
        g2 = [1, 1]
    t = {ls[0][1]: (g1[1]+g2[0]), ls[0][0]: (g1[0]+g2[1])}
    return t