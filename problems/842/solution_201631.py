    v1 = 0
    v2 = 0
    e1 = 0
    e2 = 0
    if ((I[0][2][0]) > (I[0][2][1])):
        v1 = v1 + 1
    if ((I[0][2][0]) < (I[0][2][1])):
        v2 = v2 + 1
    if ((I[0][2][0]) == (I[0][2][1])):
        e1 = e1 + 1
        e2 = e2 + 1
    if ((I[1][2][1]) > (I[1][2][0])):
        v1 = v1 + 1
    if ((I[1][2][1]) < (I[1][2][0])):
        v2 = v2 + 1
    if ((I[1][2][1]) == (I[1][2][0])):
        e1 = e1 + 1
        e2 = e2 + 1
    pontos1 = (3*v1+e1)
    pontos2 = (3*v2+e2)
