def quant_palavras(frase):
    """Dada ums frase, essa funçao retorna o numero de
    palavras dentro da frase"""
    lista_palavras_frase=split(frase)
    numeroPalavrasFrase=len(lista_palavras_frase)
    return numeroPalavrasFrase