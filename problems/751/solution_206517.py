def quant_palavras(frase):
    """Função que dada uma frase, retorna o número de palavras da frase.
       
       Parameters:
       frase: Frase escolhida para ser analisada
       
       Returns:
       Retorna o número de palavras da frase.
       str -> int"""
    q = frase
    return str.split(q)