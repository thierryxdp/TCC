def quant_palavras(frase):
    """
    Função que tem como entrada uma frase e retorna o número de palavras nela contida
    string -> int
    
    Parâmetros:
    	frase
        	String
            
    Retorna:
    	Um número inteiro que corresponde à quantidade de palavras 
    
    """
    
    
    return len(str.split(frase))