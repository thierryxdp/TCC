def maiores(l,n):
    '''dada uma lista de números inteiros e um número inteiro n, retorna outra lista  
    que contenha todos os números da lista original maiores que n, ordenados em ordem crescente'''
    l.sort()
    a=len()
    i=0
    h=[]
    while i<a:
        if l[i]>n:
            h.appendl[i]
        i=i=1
    return h