def maiores(lista, n):
    """
    	Recebe uma lista e um número <n>.
        Retorna uma nova lista com todos elementos da lista passada
        que são maiores que n.
        list, int --> list
    """
    novaLista = []
    for elemento in lista:
        if elemento > n:
            list.append(novaLista, elemento)
    return list.sort(novaLista)