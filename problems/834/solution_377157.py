def media_matriz(matriz):
    '''função que dada uma matriz de inteiros nao vazia, retorna a media de todos os numero da matriz.
    list -> float'''
    lista = []
    a = 0
    for i in range(len(matriz)):
        for j in range(len(matriz[i])):
            a = list.append(lista, matriz[i][j])
            soma = 0
            for somalista in lista:
                soma = soma + somalista
    return round(soma/(len(matriz)*len(matriz[i])),2)