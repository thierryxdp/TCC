def media_matriz(matriz):
    lista = []
    a = 0
    for i in range(len(matriz)):
        for j in range(len(matriz[i])):
            a = list.append(lista, matriz[i][j])
            soma = 0
            for somalista in lista:
                soma = soma + somalista
    return soma/(len(matriz) + len(matriz[i]))