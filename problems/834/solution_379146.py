def media_matriz(matriz):
    nlinhas=len(matriz)
    mcolunas=len(matriz[0])
    soma=[]
    for i in range(nlinhas):
        lista=[]
        for j in range(mcolunas):
            lista.append(matriz[i][j])
        soma.append(lista)
        variavel_soma=sum(soma.append(lista))
        variavel_tamanho=len(soma.append(lista))
    return variavel_soma/variavel_tamanho