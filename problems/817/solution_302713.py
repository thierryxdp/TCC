def acima_da_media(numeros,n):
    '''função chamada acima_da_media que dada uma lista com as
    notas dos alunos de uma turma, retorna uma lista 
    ordenada com as notas que ficaram acima da média.
    list,int->list'''
    list.append(numeros,n)
    list.sort(numeros)
    eliminar=list.index(numeros,n)
    eliminado=eliminar+1
    maioresN=numeros[eliminado:]
    return maioresN