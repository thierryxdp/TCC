def substitui(s, x, i):
    """Função que recebe uma string s, um caractere x
    e um número inteiro i entre 0 e o comprmento da
    string, e retorna uma string igual a s, exceto que
    o elemento da posição i deve ser substituido pelo
    caractere x.
    str,str,int->str
    """
    return s[0:i] + x