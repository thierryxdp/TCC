def inverte(texto):
    exc = str.split(str.split(str.split(str.split(str.split(str.split(str.split(str.split(str.split(texto,'.',' '), ',',' '),':',' '),';',' '),';',' '),'!',' '),'?',' '),'...',' '),'-',' ')                                                                                              
    sep = list(str.split(exc," "))
    inversa = sep[-1:0:-1]
    return str.join(" ",inversa)