def inverte(frase):
    ''' retorna a frase com todos os caracteres de pontuacao
    substituidos por espaÃ§o; str->str'''
    if '-' in frase:
        frase = frase.replace('-',' ')
    if ',' in frase:
        frase = frase.replace(',',' ')
    if ':' in frase:
        frase = frase.replace(':',' ')
    if ';' in frase:
        frase = frase.replace(';',' ')
    if '.' in frase:
        frase = frase.replace('.',' ')
    if '?' in frase:
        frase = frase.replace('?',' ')
    if '!' in frase:
        frase = frase.replace('!',' ')
    return frase