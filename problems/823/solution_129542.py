def faltante(lista):
    '''Função que identifica qual numero da peça que está faltando dada 
    uma lista de numeros como entrada.
    lista -> int'''
    
    indice = 0
    
    while indice < (len(lista)-1):
        lista[indice] > lista[indice+1]
        lista.append(indice)
        indice =+ 1
    return lista