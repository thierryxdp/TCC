def faltante(lista):
    '''retorna o numero faltante na lista dada'''
    '''list -> int'''
    if lista[0]!=1:
        return lista[0]
    elif lista[1]!=2:
        return lista[1]
    elif lista[2]!=3:
        return lista[2]
    elif lista[3]!=4:
        return lista[3]
    elif lista[4]!=5:
        return lista[4]
    elif lista[5]!=6:
        return lista[5]
    elif lista[6]!=7:
        return lista[6]
    elif lista[7]!=8:
        return lista[7]