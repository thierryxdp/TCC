def faltante (lista):
    '''
    	essa função serve para identificar qual número está faltando em uma lista de inteiros
        de tamanho não específico
        list->int
    '''
    if 1 not in lista:
        return 1
    if 2 not in lista:
        return 2
    if 3 not in lista:
        return 3
    if 4 not in lista:
        return 4