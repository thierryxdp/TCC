def filtra_pares(tupla):
    '''retorna uma nova tupla com os elementos pares de uma tupla com 4 elementos inteiros de entrada
    tup -> tup'''
    if (tupla[0])%2==0:
         w = (tupla[0],)
    if (tupla[1])%2==0:
         x = (tupla[1],)
    if (tupla[2])%2==0:
         y = (tupla[2],)        
    if (tupla[3])%2==0:
         z = (tupla[3],)
    return w+x+y+z