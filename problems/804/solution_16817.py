def filtra_pares(a,b,c,d):
    '''filtra valores pares dentro de uma tupla e retorna em outra tupla apenas com os pares;int, int, int, int->tuple'''
    if(a%2==0):
        return str(a)
    if(b%2==0):
        return str(b)
    if(c%2==0):
        return str(c)
    if(d%2==0):
        return str(d)