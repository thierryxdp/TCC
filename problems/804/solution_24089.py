def filtra_pares(a,b,c,d):
    '''Função que recebe uma tupla e retorna a mesma tupla
    com somente elementos pares; tupla->tupla'''
    nova=()
    if a%2==0:
        return a in nova
    elif b%2==0:
        return b in nova
    elif c%2==0:
        return c in nova
    elif d%2==0:
        return d in nova