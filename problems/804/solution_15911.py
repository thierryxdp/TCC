def filtra_pares (a, b, c, d):
    """Retorna os em formato de tupla os números pares de uma tupla de quatro números inserida.
       Entrada: tupla
       Saída: tupla
       """
    if a/2 == int(a/2):
        return a
    else:
        return ""
    if b/2 == int(b/2):
        return b
    else:
        return ""
    if c/2 == int(c/2):
        return c
    else:
        return ""
    if d/2 == int(d/2):
        return d
    else:
        return ""