def filtra_pares(l):
    """Informe uma trupla com 4 números e a função irá retornar os números pares
        number, number, number, number -> string"""
    if(l[0]%2==0):
        varA = l[0]
    if(l[1]%2==0):
        varB = l[1]
    if l[2]%2==0:
        varC = l[2]
    if(l[3]%2==0):
        varD = l[3]      
    return varA,varB,varC,varD