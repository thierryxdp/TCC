def filtra_pares(n1,n2,n3,n4):
    '''Retorna uma tupla contendo apenas os nÃºmeros pares 
    de tupla original;
    int,int,int,int->int'''
    pares=()
    if n1%2:
        return pares.append(n1)
    if n2%2:
        return pares.append(n2)
    if n3%2:
        return pares.append(n3)
    if n4%2:
        return pares.append(n4)