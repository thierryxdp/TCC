    """Calacula e retorna uma nova tupla com numeros pares de uma tupla original"""
    a%2==0
    b%2==0
    c%2==0
    d%2==0
    if (a<0) % 2==0:
        return a
    if (a>0) % 2==0:
        return -a
    if (b<0) % 2==0:
        return b
    if (b>0) % 2==0:
        return -b
    if (c<0) % 2==0:
        return c
    if (c>0) % 2==0:
        return -c
    if (d<0) % 2==0:
        return d
    if (d>0) % 2==0:
        return -d