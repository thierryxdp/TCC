def filtra_pares(f):
    '''função que recebe uma tupla t
    com 4 elementos inteiros como parâmetro
    e retorna uma nova tupla contendo apenas
    os elementos pares
    '''
  
    e1=f[0]/2               
    e2=f[1]/2
    e3=f[2]/2
    e4=f[3]/2
    
    if e1 == int and e2 == int and e3 == int and e4 == int:
        return (e1,e2,e3,e4)