def filtra_pares(a,b,c,d):
    """função que recebe uma tupla com quatro elementos inteiros e retorna uma tupla contendo apenas os elementos pares da tupla original, na mesma ordem que se encontravam"""
    if a%2==0:
        return a
    if b%2==0:
        return b
    if c%2==0:
        return c
    if d%2==0:
        return d