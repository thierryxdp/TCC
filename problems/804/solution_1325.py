def filtra_pares(elem1,elem2,elem3,elem4):
    """Função recebe uma tupla com quatro elementos inteiros, e retorna
    uma nova tupla contendo apenas os elementos pares da tupla original
    entrada: int, int, int, int
    retorno: int"""
    if (elem1 % 2 == 0):
        return elem1
    if (elem2 % 2 == 0):
        return elem2
    if (elem3 % 2 == 0):
        return elem3
    if (elem4 % 2 == 0):
        return elem4