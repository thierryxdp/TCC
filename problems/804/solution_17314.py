def filtra_pares(w,x,y,z):
    tupla_par=tuple()
    if w % 2==0:
        return tupla_par+w
    else:
        return tupla_par
    if x % 2==0:
        return tupla_par+x
    else:
        return tupla_par
    if y % 2==0:
        return tupla_par+y
    else:
        return tupla_par
    if z % 2==0:
        return tupla_par+z
    else:
        return tupla_par
    return tupla_par