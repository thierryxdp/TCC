def filtra_pares(tupla):
    tupla_par = ()
    tupla[0] = a
    tupla[1] = b
    tupla[2] = c
    tupla[3] = d
    if a%2 == 0:
        tupla_par = tupla_par + (a)
    if b%2 == 0:
        tupla_par = tupla_par + (b)
    if c%2 == 0:
        tupla_par = tupla_par + (c)
    if d%2 == 0:
        tupla_par = tupla_par + (d)
    return tupla_par