def filtra_pares(tup1, tup2, tup3, tup4):
    '''Função que recebe uma tupla com 4 elementos e devolve uma tupla contendo apenas os elementos pares'''
    final = ()
    if (tup1)%2 == 0:
        final + tup1
    if (tup2)%2 == 0:
        final + tup2
    if (tup3)%2 == 0:
        final + tup3
    if (tup4)%2 == 0:
        final + tup4
    return final