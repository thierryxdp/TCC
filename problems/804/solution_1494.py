def filtra_pares(tupla):
    '''funcao que filtra somente os elemetnos pares em um tupla
    tupla->tupla'''
    for x in tupla:
        if x%2==0:
            return x
    for y in tupla:
        if y%2==0:
            return y
    for z in tupla:
        if z%2==0:
            return z
    for d in tupla:
        if d%2==0:
            return d