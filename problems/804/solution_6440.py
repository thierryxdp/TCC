def filtra_pares(x1,x2,x3,x4):
    '''Função que recebe uma tupla com 4 elementos inteiros e retorna uma nova tupla contendo apenas elementos pares'''
    if (x1)/2 == int:
        return (x1)
    if (x2)/2 == int:
        return (x2)
    if (x3)/2 == int:
        return (x3)
    if (x4)/2 == int:
        return (x4)
    else:
        return (x1,x2,x3,x4)