def filtra_pares(tupla):
    """Mostra os elementos pares da tupla de elementos inteiros
    	tupla -> tupla
    	Parameters:
        tupla: Tupla com quatro elementos inteiros
        
        Returns:
        Os elementos pares da tupla de quatro elementos inteiros fornecida
     """
    item1 = tupla[0]
    item2 = tupla[1]
    item3 = tupla[2]
    item4 = tupla[3]
    item5 = tupla[4]
    item6 = tupla[5]
    
    if item1//2 == 0:
        return item1
    if item2//2 == 0:
        return item2
    if item3//2 == 0:
        return item3
    if item4//2 == 0:
        return item4
    if item5//2 == 0:
        return item5
    if item6//2 == 0:
        return item6