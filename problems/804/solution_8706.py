def filtra_pares(tupla):
    """Retorna uma nova tupla somente com os nÃºmeros pares da tupla original
    tuple -> tuple"""
    nova_tupla= []
    if tupla[0] % 2 == 0:
        tupla [0] += nova_tupla
    if tupla[1] % 2 == 0:
        tupla [1] += nova_tupla
    if tupla[2] % 2 == 0:
        tupla [2] += nova_tupla
    if tupla[3] % 2 == 0:
        tupla [3] += nova_tupla
    return nova_tupla