def conta_frases(texto):
        """A entrada será um texto contido no parâmetro
        e se retornará o número de frases contidas nele"""
        # string -> string
       
        textocomponto = (".")
        texto.replace ("...", ".")
        texto.replace ("!", ".")
        texto.replace ("?", ".")
        texto.split (".")
        return len (textocomponto)