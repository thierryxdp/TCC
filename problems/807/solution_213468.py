def conta_frases(texto):
    """retorna o número de frases do texto. Cada frase é terminada com um ponto final, um ponto de exclamação, um ponto de interrogação ou reticências
    str->"""
    str.count(texto,'.')
    str.count(texto,'!')
    str.count(texto,'?')
    str.count(texto,'...')
    return (range(str.count))